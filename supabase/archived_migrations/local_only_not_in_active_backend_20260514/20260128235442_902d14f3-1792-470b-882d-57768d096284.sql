-- Create er_commence table
CREATE TABLE public.er_commence (
    commence_seq_no bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    regno varchar(6) NOT NULL,
    date_commenced timestamp(3),
    date_ceased timestamp(3),
    date_modified timestamp(3),
    modified_by varchar(30)
);

-- Create er_last_regno table
CREATE TABLE public.er_last_regno (
    regno varchar(6) PRIMARY KEY,
    date_issued timestamp(3) NOT NULL
);

-- Create er_locations table
CREATE TABLE public.er_locations (
    regno varchar(6) NOT NULL,
    location_id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    trade_name varchar(40),
    loc_addr1 varchar(25),
    loc_addr2 varchar(25),
    activity_type varchar(50),
    PRIMARY KEY (regno, location_id)
);

-- Create er_master table
CREATE TABLE public.er_master (
    regno varchar(6) PRIMARY KEY,
    name varchar(40) NOT NULL,
    trade_name varchar(40),
    phone varchar(10),
