// DOCX Export
// ────────────────────────────────────────────

const cellBorder = { style: BorderStyle.SINGLE, size: 1, color: 'CCCCCC' };
const cellBorders = { top: cellBorder, bottom: cellBorder, left: cellBorder, right: cellBorder };

export async function exportDocx(
  columns: ExportColumn[],
  data: Record<string, any>[],
  metadata: ExportMetadata,
  fileName: string,
) {
  // Calculate column widths — distribute evenly across US Letter content width
  const contentWidth = 9360; // US Letter with 1" margins in DXA
  const colWidth = Math.floor(contentWidth / columns.length);
  const columnWidths = columns.map(() => colWidth);

  // Metadata paragraphs
  const metaParagraphs: Paragraph[] = [
    new Paragraph({ spacing: { after: 100 }, children: [
      new TextRun({ text: `Generated: ${metadata.generatedDate}`, size: 18, color: '666666', italics: true }),
      ...(metadata.generatedBy ? [new TextRun({ text: `  |  By: ${metadata.generatedBy}`, size: 18, color: '666666', italics: true })] : []),
    ]}),
    new Paragraph({ spacing: { after: 100 }, children: [
      new TextRun({ text: `Total Records: ${metadata.totalRecords}`, size: 18, color: '666666' }),
    ]}),
  ];

  if (metadata.filtersApplied.length > 0) {
    metaParagraphs.push(new Paragraph({ spacing: { after: 200 }, children: [
      new TextRun({ text: 'Filters: ', size: 18, bold: true, color: '333333' }),
      new TextRun({ text: metadata.filtersApplied.map(f => `${f.label}: ${f.value}`).join(', '), size: 18, color: '666666' }),
    ]}));
  }

  // Header row
  const headerRow = new TableRow({
    children: columns.map((col, i) => new TableCell({
      borders: cellBorders,
      width: { size: columnWidths[i], type: WidthType.DXA },
      shading: { fill: '0E5F3A', type: ShadingType.CLEAR },
      margins: { top: 60, bottom: 60, left: 80, right: 80 },
      children: [new Paragraph({ children: [new TextRun({ text: col.header, bold: true, color: 'FFFFFF', size: 18, font: 'Arial' })] })],
    })),
  });

  // Data rows
  const dataRows = data.map((row, idx) => new TableRow({
    children: columns.map((col, i) => {
      const v = row[col.key];
      const text = v === null || v === undefined ? '—' : String(v);
      return new TableCell({
        borders: cellBorders,
        width: { size: columnWidths[i], type: WidthType.DXA },
        shading: idx % 2 === 1 ? { fill: 'F8FAFC', type: ShadingType.CLEAR } : undefined,
        margins: { top: 40, bottom: 40, left: 80, right: 80 },
        children: [new Paragraph({ children: [new TextRun({ text, size: 18, font: 'Arial' })] })],
      });
    }),
  }));

  const table = new Table({
    width: { size: contentWidth, type: WidthType.DXA },
    columnWidths,
    rows: [headerRow, ...dataRows],
  });

  const doc = new Document({
    styles: {
      default: { document: { run: { font: 'Arial', size: 22 } } },
    },
    sections: [{
      properties: {
        page: {
          size: { width: 12240, height: 15840 },
          margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 },
        },
      },
      headers: {
        default: new Header({
          children: [
            new Paragraph({
              spacing: { after: 100 },
              children: [
                new TextRun({ text: SSB_BRAND.name, bold: true, size: 20, color: '0E5F3A', font: 'Arial' }),
              ],
            }),
          ],
        }),
      },
      footers: {
        default: new Footer({
          children: [
            new Paragraph({
              alignment: AlignmentType.CENTER,
              children: [
                new TextRun({ text: `${SSB_BRAND.name} — ${SSB_BRAND.address}  |  Page `, size: 14, color: '999999' }),
                new TextRun({ children: [PageNumber.CURRENT], size: 14, color: '999999' }),
              ],
            }),
          ],
        }),
      },
      children: [
        new Paragraph({
          heading: HeadingLevel.HEADING_1,
          spacing: { after: 200 },
          children: [new TextRun({ text: metadata.title, bold: true, size: 28, color: '0E5F3A', font: 'Arial' })],
        }),
        ...metaParagraphs,
        table,
      ],
    }],
  });

  const buffer = await Packer.toBuffer(doc);
  saveAs(new Blob([buffer as unknown as ArrayBuffer], { type: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document' }), `${fileName}.docx`);
}

// ────────────────────────────────────────────
// Grouped Excel
// ────────────────────────────────────────────

export async function exportGroupedExcel(
  columns: ExportColumn[],
  data: Record<string, any>[],
  groupByKey: string,
  groupByLabel: string,
  metadata: ExportMetadata,
  fileName: string,
) {
  const wb = new ExcelJS.Workbook();

  // Group data
  const groups: Record<string, any[]> = {};
  data.forEach(row => {
    const gv = row[groupByKey] || 'Unspecified';
    if (!groups[gv]) groups[gv] = [];
    groups[gv].push(row);
  });

  Object.entries(groups).sort((a, b) => a[0].localeCompare(b[0])).forEach(([groupName, groupData]) => {
    const sheetName = groupName.slice(0, 31); // Excel max sheet name
    const ws = wb.addWorksheet(sheetName);

    // Header
    ws.columns = columns.map(c => ({ header: c.header, key: c.key, width: c.width || 18 }));

    // Group title row
    ws.spliceRows(1, 0, [`${groupByLabel}: ${groupName} (${groupData.length} records)`]);
    ws.mergeCells(1, 1, 1, columns.length);
    const titleCell = ws.getCell('A1');
    titleCell.font = { bold: true, size: 12, color: { argb: 'FF0E5F3A' } };
    titleCell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FFF8FAFC' } };

    // Column headers (row 2)
    const headerRow = ws.getRow(2);
    headerRow.font = { bold: true, color: { argb: 'FFFFFFFF' } };
    headerRow.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF0E5F3A' } };

    // Data
    groupData.forEach(row => {
      const mapped: Record<string, any> = {};
      columns.forEach(c => { mapped[c.key] = row[c.key] ?? ''; });
      ws.addRow(mapped);
    });

    // Borders
    ws.eachRow(row => {
      row.eachCell(cell => {
        cell.border = { top: { style: 'thin' }, bottom: { style: 'thin' }, left: { style: 'thin' }, right: { style: 'thin' } };
      });
    });
  });

  const buffer = await wb.xlsx.writeBuffer();
  saveAs(new Blob([buffer], { type: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' }), `${fileName}.xlsx`);
}
