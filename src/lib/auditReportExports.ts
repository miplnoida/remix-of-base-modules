/**
 * Centralized export utilities for Audit Universe and Risk Management modules.
 * Provides PDF, DOCX, and grouped report generation with SSB branding.
 */

import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';
import { addSSBHeader, addSSBFooter, getSSBTableConfig, SSB_BRAND } from '@/lib/reportTemplate';
import ExcelJS from 'exceljs';
import { saveAs } from 'file-saver';
import {
  Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell,
  Header, Footer, AlignmentType, WidthType, ShadingType, BorderStyle,
  PageNumber, HeadingLevel,
} from 'docx';
import type { ExportColumn } from '@/components/common/ExportDropdown';

// ────────────────────────────────────────────
// Types
// ────────────────────────────────────────────

export interface ExportMetadata {
  title: string;
  generatedDate: string;
  generatedBy?: string;
  filtersApplied: { label: string; value: string }[];
  totalRecords: number;
}

export interface GroupByOption {
  label: string;
  key: string;
}

// ────────────────────────────────────────────
// Metadata helpers
// ────────────────────────────────────────────

export function buildMetadata(
  title: string,
  totalRecords: number,
  filters: { label: string; value: string }[],
  generatedBy?: string,
): ExportMetadata {
  return {
    title,
    generatedDate: new Date().toLocaleDateString(),
    generatedBy,
    filtersApplied: filters.filter(f => f.value && f.value !== 'all'),
    totalRecords,
  };
}

// ────────────────────────────────────────────
// PDF with metadata
// ────────────────────────────────────────────

export function exportPDFWithMetadata(
  columns: { header: string; key: string }[],
  data: Record<string, any>[],
  metadata: ExportMetadata,
  fileName: string,
  orientation: 'portrait' | 'landscape' = 'portrait',
) {
  const doc = new jsPDF({ orientation });

  const additionalInfo: { label: string; value: string }[] = [
    { label: 'Generated', value: metadata.generatedDate },
    { label: 'Total Records', value: String(metadata.totalRecords) },
  ];
  if (metadata.generatedBy) {
    additionalInfo.push({ label: 'Generated By', value: metadata.generatedBy });
  }
  metadata.filtersApplied.forEach(f => {
    additionalInfo.push({ label: f.label, value: f.value });
  });

  const startY = addSSBHeader(doc, {
    title: metadata.title,
    subtitle: `Total Records: ${metadata.totalRecords}`,
    orientation,
    additionalInfo,
  });

  const headers = columns.map(c => c.header);
  const body = data.map(row =>
    columns.map(c => {
      const v = row[c.key];
      if (v === null || v === undefined) return '—';
      if (typeof v === 'number') return v.toLocaleString();
      return String(v);
    })
  );

  autoTable(doc, { head: [headers], body, ...getSSBTableConfig(startY) });
  addSSBFooter(doc);
  doc.save(`${fileName}.pdf`);
}

// ────────────────────────────────────────────
// Grouped PDF
// ────────────────────────────────────────────

export function exportGroupedPDF(
  columns: { header: string; key: string }[],
  data: Record<string, any>[],
  groupByKey: string,
  groupByLabel: string,
  metadata: ExportMetadata,
  fileName: string,
) {
  const doc = new jsPDF({ orientation: 'landscape' });

  const additionalInfo: { label: string; value: string }[] = [
    { label: 'Generated', value: metadata.generatedDate },
    { label: 'Total Records', value: String(metadata.totalRecords) },
    { label: 'Grouped By', value: groupByLabel },
  ];
  metadata.filtersApplied.forEach(f => additionalInfo.push(f));

  let y = addSSBHeader(doc, {
    title: `${metadata.title} — By ${groupByLabel}`,
    subtitle: `Total Records: ${metadata.totalRecords}`,
    orientation: 'landscape',
    additionalInfo,
  });

  // Group data
  const groups: Record<string, any[]> = {};
  data.forEach(row => {
    const gv = row[groupByKey] || 'Unspecified';
    if (!groups[gv]) groups[gv] = [];
    groups[gv].push(row);
  });

  const headers = columns.map(c => c.header);
  let isFirst = true;

  Object.entries(groups).sort((a, b) => a[0].localeCompare(b[0])).forEach(([groupName, groupData]) => {
    if (!isFirst) doc.addPage();
    isFirst = false;

    // Group header
    const pw = doc.internal.pageSize.getWidth();
    doc.setFillColor(248, 250, 252);
    doc.rect(14, y - 2, pw - 28, 10, 'F');
    doc.setFontSize(11);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(14, 95, 58);
    doc.text(`${groupByLabel}: ${groupName} (${groupData.length} records)`, 16, y + 5);
    y += 14;

    const body = groupData.map(row =>
      columns.map(c => {
        const v = row[c.key];
        if (v === null || v === undefined) return '—';
        return String(v);
      })
    );

    autoTable(doc, {
      head: [headers],
      body,
      ...getSSBTableConfig(y),
    });

    y = (doc as any).lastAutoTable?.finalY + 8 || y + 20;
  });

  addSSBFooter(doc);
  doc.save(`${fileName}.pdf`);
}

// ────────────────────────────────────────────
// Risk Detail PDF
// ────────────────────────────────────────────

export function exportRiskDetailPDF(
  risk: any,
  actions: any[],
  reviews: any[],
  fileName: string,
) {
  const doc = new jsPDF();

  const startY = addSSBHeader(doc, {
    title: 'Risk Detail Report',
    subtitle: risk.risk_title,
    additionalInfo: [
      { label: 'Entity', value: risk.ia_audit_universe?.entity_name || '—' },
      { label: 'Owner', value: risk.risk_owner || '—' },
      { label: 'Status', value: risk.status },
      { label: 'Generated', value: new Date().toLocaleDateString() },
    ],
  });

  // Scoring table
  autoTable(doc, {
    head: [['Metric', 'Likelihood', 'Impact', 'Score', 'Level']],
    body: [
      ['Inherent', String(risk.inherent_likelihood), String(risk.inherent_impact), String(risk.inherent_risk_score), risk.inherent_risk_level],
      ['Residual', String(risk.residual_likelihood), String(risk.residual_impact), String(risk.residual_risk_score), risk.residual_risk_level],
    ],
    ...getSSBTableConfig(startY),
  });

  let y = (doc as any).lastAutoTable?.finalY + 10 || startY + 30;

  // Description
  if (risk.risk_description) {
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(14, 95, 58);
    doc.text('Description', 14, y);
    y += 5;
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(30, 30, 30);
    doc.setFontSize(9);
    const lines = doc.splitTextToSize(risk.risk_description, 180);
    doc.text(lines, 14, y);
    y += lines.length * 4 + 6;
  }

  // Mitigation actions
  if (actions.length > 0) {
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(14, 95, 58);
    doc.text(`Mitigation Actions (${actions.length})`, 14, y);
    y += 4;

    autoTable(doc, {
      head: [['Action', 'Assigned To', 'Due Date', 'Priority', 'Status']],
      body: actions.map(a => [
        a.action_title,
        a.assigned_to || '—',
        a.due_date || '—',
        a.priority,
        a.status,
      ]),
      ...getSSBTableConfig(y),
    });
    y = (doc as any).lastAutoTable?.finalY + 10 || y + 20;
  }

  // Review history
  if (reviews.length > 0) {
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(14, 95, 58);
    doc.text(`Review History (${reviews.length})`, 14, y);
    y += 4;

    autoTable(doc, {
      head: [['Date', 'Reviewer', 'Comment', 'Level Change']],
      body: reviews.map(r => [
        r.review_date || '—',
        r.reviewed_by || '—',
        r.comments || '—',
        r.previous_risk_level !== r.new_risk_level ? `${r.previous_risk_level} → ${r.new_risk_level}` : 'No change',
      ]),
      ...getSSBTableConfig(y),
    });
  }

  addSSBFooter(doc);
  doc.save(`${fileName}.pdf`);
}

// ────────────────────────────────────────────
// Mitigation Action Plan PDF
// ────────────────────────────────────────────

export function exportMitigationPlanPDF(
  risks: any[],
  allActions: any[],
  fileName: string,
) {
  const doc = new jsPDF({ orientation: 'landscape' });

  // Summary counts
  const planned = allActions.filter(a => a.status === 'Planned').length;
  const inProgress = allActions.filter(a => a.status === 'In Progress').length;
  const completed = allActions.filter(a => a.status === 'Completed').length;
  const overdue = allActions.filter(a => a.due_date && a.due_date < new Date().toISOString().slice(0, 10) && a.status !== 'Completed').length;

  const startY = addSSBHeader(doc, {
    title: 'Mitigation Action Plan',
    orientation: 'landscape',
    additionalInfo: [
      { label: 'Total Actions', value: String(allActions.length) },
      { label: 'Planned', value: String(planned) },
      { label: 'In Progress', value: String(inProgress) },
      { label: 'Completed', value: String(completed) },
      { label: 'Overdue', value: String(overdue) },
      { label: 'Generated', value: new Date().toLocaleDateString() },
    ],
  });

  // Group by risk
  const grouped: Record<string, { risk: any; actions: any[] }> = {};
  allActions.forEach(a => {
    const riskId = a.risk_id;
    if (!grouped[riskId]) {
      const risk = risks.find(r => r.id === riskId);
      grouped[riskId] = { risk: risk || { risk_title: 'Unknown Risk' }, actions: [] };
    }
    grouped[riskId].actions.push(a);
  });

  let y = startY;
  let isFirst = true;

  Object.values(grouped).forEach(({ risk, actions }) => {
    if (!isFirst && y > doc.internal.pageSize.getHeight() - 60) {
      doc.addPage();
      y = 20;
    }
    isFirst = false;

    const pw = doc.internal.pageSize.getWidth();
    doc.setFillColor(248, 250, 252);
    doc.rect(14, y - 2, pw - 28, 8, 'F');
    doc.setFontSize(10);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(14, 95, 58);
    doc.text(`Risk: ${risk.risk_title}`, 16, y + 4);
    y += 10;

    autoTable(doc, {
      head: [['Action', 'Assigned To', 'Due Date', 'Priority', 'Status']],
      body: actions.map(a => [a.action_title, a.assigned_to || '—', a.due_date || '—', a.priority, a.status]),
      ...getSSBTableConfig(y),
    });

    y = (doc as any).lastAutoTable?.finalY + 8 || y + 20;
  });

  addSSBFooter(doc);
  doc.save(`${fileName}.pdf`);
}

